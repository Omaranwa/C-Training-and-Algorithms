#include <math.h>
#include <stdio.h>
int main()
{
    
    // find the square root of a number, use the sqrt() function
    printf("%f\n", sqrt(16));
    // ceil() function rounds a number upwards to its nearest integer
    // floor() method rounds a number downwards to its nearest integer
    printf("%f\n", ceil(1.4));
    printf("%f\n", floor(1.4));

    // pow() function returns the value of x to the power of y (xy)
    printf("%f", pow(4, 3));

    // for other math functions
    // https://www.geeksforgeeks.org/c-library-math-h-functions/
    
}
